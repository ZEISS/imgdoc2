        [Fact]
        public void DecodeZstd1Gray16()
        {
            byte[] jpgXrGray16 = GetZstd1Gray16();

            // this test is operating on "interop"-level
            var instance = ImgDoc2ApiInterop.Instance;
            var (decompressed, decompressedStride) = instance.DecodeZstd1(jpgXrGray16, PixelType.Gray16, 32, 32);

            var computedHash = CalculateMd5Hash(32 * 2, 32, decompressedStride, decompressed.Span);
            byte[] expectedHash = new byte[] { 0x9b, 0xe3, 0x65, 0x99, 0x9a, 0xce, 0xcc, 0xb8, 0xa7, 0xc6, 0x2f, 0x0b, 0xa4, 0xd9, 0xce, 0x90 };
            computedHash.Should().Equal(expectedHash, because: "the computed hash should match the expected hash");
        }

        private static byte[] CalculateMd5Hash(int lineSize, int height, int stride, Span<byte> imageData)
        {
            using (var md5 = MD5.Create())
            {
                byte[] tempRow = new byte[lineSize];

                for (int row = 0; row < height; row++)
                {
                    // Calculate the start position of the row.
                    int rowStart = row * stride;

                    // Copy the valid part of the row into the temporary buffer.
                    Span<byte> currentRow = imageData.Slice(rowStart, lineSize);
                    currentRow.CopyTo(tempRow);

                    // Update the hash with the data from this row.
                    md5.TransformBlock(tempRow, 0, tempRow.Length, null, 0);
                }

                // Finalize the hash calculation.
                md5.TransformFinalBlock(Array.Empty<byte>(), 0, 0);

                // Return the computed hash.
                var hash = md5.Hash ?? throw new InvalidOperationException("The hash is null.");
                return hash;
            }
        }

