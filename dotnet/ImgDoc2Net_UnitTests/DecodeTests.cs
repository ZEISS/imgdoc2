        [Fact]
        public void DecodeJpgXrBgr24()
        {
            byte[] jpgXrBgr24 = GetJpgXrBgr24();

            // this test is operating on "interop"-level
            var instance = ImgDoc2ApiInterop.Instance;
            var (decompressed, decompressedStride) = instance.DecodeJpgXr(jpgXrBgr24, PixelType.Bgr24, 120, 55);

            var computedHash = CalculateMd5Hash(120 * 3, 55, decompressedStride, decompressed.Span);
            byte[] expectedHash = new byte[] { 0x04, 0x77, 0x2f, 0x32, 0x2f, 0x94, 0x9b, 0x07, 0x0d, 0x53, 0xa5, 0x24, 0xea, 0x64, 0x5a, 0x1a };
